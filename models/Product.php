<?php


namespace app\models;
use yii\db\ActiveRecord;
use Yii;
class Product extends ActiveRecord
{
    public function rules()
    {
        return [
            [['name', 'articul','price','count'], 'required']

        ];


    }

    public function search($str){
        $sql = "SELECT * FROM `product` WHERE `name` LIKE '%$str%' OR `articul` LIKE '%$str%'";
        return Yii::$app->db->createCommand($sql)->queryAll();
    }
    public function rusInTranslit($string) {
        $converter = array(
            'а' => 'a',   'б' => 'b',   'в' => 'v',
            'г' => 'g',   'д' => 'd',   'е' => 'e',
            'ё' => 'e',   'ж' => 'zh',  'з' => 'z',
            'и' => 'i',   'й' => 'y',   'к' => 'k',
            'л' => 'l',   'м' => 'm',   'н' => 'n',
            'о' => 'o',   'п' => 'p',   'р' => 'r',
            'с' => 's',   'т' => 't',   'у' => 'u',
            'ф' => 'f',   'х' => 'h',   'ц' => 'c',
            'ч' => 'ch',  'ш' => 'sh',  'щ' => 'sch',
            'ь' => '',    'ы' => 'y',   'ъ' => '',
            'э' => 'e',   'ю' => 'yu',  'я' => 'ya',

            'А' => 'A',   'Б' => 'B',   'В' => 'V',
            'Г' => 'G',   'Д' => 'D',   'Е' => 'E',
            'Ё' => 'E',   'Ж' => 'Zh',  'З' => 'Z',
            'И' => 'I',   'Й' => 'Y',   'К' => 'K',
            'Л' => 'L',   'М' => 'M',   'Н' => 'N',
            'О' => 'O',   'П' => 'P',   'Р' => 'R',
            'С' => 'S',   'Т' => 'T',   'У' => 'U',
            'Ф' => 'F',   'Х' => 'H',   'Ц' => 'C',
            'Ч' => 'Ch',  'Ш' => 'Sh',  'Щ' => 'Sch',
            'Ь' => '',    'Ы' => 'Y',   'Ъ' => '',
            'Э' => 'E',   'Ю' => 'Yu',  'Я' => 'Ya',

            '/' => '',
        );
        return strtr($string, $converter);
    }

    public function stringTranslit($string) {
        $string = $this->rusInTranslit($string);
        $string = strtolower($string);
        $string = preg_replace('~[^-a-z0-9_]+~u', '-', $string);
        $string = trim($string, "-");
        return $string;
    }
}